    <h2 id="First_Point_Header">Articolul 1: Obiectul de reglementare</h2>
    <article>
    <p id="First_Sub_Point_1">(1) Prezenta lege are ca obiect de reglementare stabilirea unui sistem de salarizare pentru personalul din sectorul bugetar platit din bugetul general consolidat al statului. </p>
    <p>(2) Prezenta lege creeaza cadrul legislativ pentru:</p>
    <p>a) dezvoltarea unui sistem de fundamentare a politicilor salariale in sectorul bugetar si de supraveghere a aplicarii acestora;</p>
    <p>b) dezvoltarea unui sistem de distribuire a resurselor bugetare alocate pentru cheltuielile de personal, prin stabilirea salariilor de baza in sectorul bugetar, in vederea promovarii performantei individuale;</p>
    <p>c) remunerarea persoanelor care ocupa functii de demnitate publica</p>
    <p>(3) Incepand cu data intrarii in vigoare a prezentei legi, drepturile salariale ale personalului prevazut la alin. (1) sunt si raman, in mod exclusiv, cele prevazute in prezenta lege. </p>
    <p>(4) Prin contractele colective de munca/acordurile colective de munca si contractele individuale de munca nu pot fi negociate salarii sau alte drepturi de natura salariala in bani sau in natura care excedeaza sau contravin prevederilor prezentei legi. </p>
  </article>

     <h2>Articolul 2: Domeniul de aplicare</h2>
   <article>
     <p>(1) Dispozitiile prezentei legi se aplica:</p>
     <p>a) personalului din autoritati si institutii publice, respectiv Parlamentul, Administratia Prezidentiala, autoritatea judecatoreasca, Guvernul, ministerele, celelalte organe de specialitate ale administratiei publice centrale, unitatile teritoriale, autoritati ale administratiei publice locale, alte autoritati publice, autoritati administrative autonome, precum si institutiile din subordinea acestora, finantate integral din bugetul de stat, bugetele locale, bugetul asigurarilor sociale de stat si bugetele fondurilor speciale;</p>
     <p>b) personalului din autoritati si institutii publice finantate din venituri proprii si subventii acordate de la bugetul de stat, bugetele locale, bugetul asigurarilor sociale de stat si bugetele fondurilor speciale;</p>
     <p>c) personalului din autoritatile si institutiile publice finantate integral din venituri proprii;</p>
     <p>d) persoanelor care sunt conducatori ai unor institutii publice in temeiul unui contract, altul decat contractul individual de munca;</p>
     <p id="2">e) persoanelor care ocupa functii de demnitate publica. </p>
     <p>(2) Dispozitiile prezentei legi nu se aplica personalului din Banca Nationala a Romaniei, Autoritatea de Supraveghere Financiara, Autoritatea Nationala de Reglementare in Domeniul Energiei si Autoritatea Nationala pentru Administrare si Reglementare in Comunicatii. </p>
     <p>(3) Intra in categoria personalului din sectorul bugetar personalul incadrat pe baza contractului individual de munca, personalul care ocupa functii de demnitate publica numite sau alese si personalul care ocupa functii asimilate functiilor de demnitate publica, magistratii, precum si personalul care beneficiaza de statute speciale, inclusiv functionarii publici si functionarii publici cu statut special. </p>
   <h2>Articolul 3: Gestionarea sistemului de salarizare</h2>
   </article>
   <p>
(1) Gestionarea sistemului de salarizare a personalului din institutiile si autoritatile publice se asigura de fiecare ordonator de credite. 
(2) Gestionarea sistemului de salarizare a personalului din institutiile de invatamant preuniversitar si universitar de stat se asigura de Ministerul Educatiei Nationale. 
(3) Gestionarea sistemului de salarizare a personalului din unitatile sanitare publice, din reteaua Ministerului Sanatatii si a autoritatilor administratiei publice locale se asigura de ordonatorii principali de credite si de Ministerul Sanatatii. 
(4) Ordonatorii de credite au obligatia sa stabileasca salariile de baza/soldele de functie/salariile de functie/soldele de grad/salariile gradului profesional detinut, gradatiile, soldele de comanda/salariile de comanda, indemnizatiile de incadrare/indemnizatiile lunare, sporurile, alte drepturi salariale in bani si in natura prevazute de lege, sa asigure promovarea personalului in functii, grade si trepte profesionale si avansarea in gradatii, in conditiile legii, astfel incat sa se incadreze in sumele aprobate cu aceasta destinatie in bugetul propriu. 

Articolul 4: Controlul aplicarii legii
Controlul aplicarii prevederilor legale privind salarizarea personalului din sectorul bugetar se realizeaza de catre Guvern, prin ministere si institutiile de aparare, ordine publica si securitate nationala, potrivit atributiilor din domeniul de activitate al acestora, precum si de alte autoritati publice centrale autonome cu atributii in domeniu . 

<p id="Second_Point_Header">Articolul 5: Raportul salariat</p>
<p>In sectorul bugetar, raportul dintre cel mai mic salariu de baza si cel mai mare salariu de baza, solda de functie/salariu de functie, indemnizatie de incadrare, indemnizatie lunara este de 1 la 12.</p>
